# contoh 1
# Penggunaan fungsi “len” pada berbagai tipe data:
print(len("Linda Novita"))
print(len([10, 20, 30]))
print(len((10, 20, 30)))

# contoh 2
# Penggunaan operator “+” pada berbagai tipe data:
print(9 + 5)
print("Linda" + "Novita" + "Juliyanti")
print([2, 3] + [1, 3])

# contoh 3
# Penggunaan fungsi “max” pada berbagai tipe data:
print(max(2, 9, 4, 0))
print(max([0, 2, 3, 4]))
print(max("Linda"))

# contoh 4
# Penggunaan metode “sort” pada berbagai tipe data:
angka = [5, 3, 7, 1, 8, 2]
angka.sort()
print(angka)

nama = ['Linda', 'Novita', 'Juliyanti']
nama.sort()
print(nama)
